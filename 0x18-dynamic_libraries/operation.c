int add(int a, int b);
int sub(int a, int b);
int mul(int a, int b);
int div(int a, int b);
int mod(int a, int b);

/**
 * div - check the code
 * @a: parametre
 * @b: parametre
*/
int div(int a, int b)
{
    return (a / b);
}
/**
* add - adds two integers
* @a: parametre
* @b: parametre
* Return: the sum
*/

int add(int a, int b)
{
    return (a + b);
}

/**
* sub - checks the code
* @a: parametre
* @b: parametre
* Return: the subtract
*/

int sub(int a, int b)
{
    return (a - b);
}

/**
* mul - multiplies two integers
* @a: parametre
* @b: parametre
* Return: the multiple
*/

int mul(int a, int b)
{
    return (a * b);
}

/**
 * mod - check code
 * @a: parameter
 * @b: parameter
 * Return: modulo
*/
int mod(int a, int b)
{
    return (a % b);
}